function openNextModal() {
    document.getElementById("modal-next").style.display = "block";
    document.getElementById("modal-background-next").style.display = "block";
}

function closeNextModal() {
    document.getElementById("modal-next").style.display = "none";
    document.getElementById("modal-background-next").style.display = "none";
}

function openDeleteModal() {
    document.getElementById("modal-delete").style.display = "block";
    document.getElementById("modal-background-delete").style.display = "block";
}

function closeDeleteModal() {
    document.getElementById("modal-delete").style.display = "none";
    document.getElementById("modal-background-delete").style.display = "none";
}